      end select


      call phys_get_pthermal(w,x,ixImin1,ixImin2,ixImax1,ixImax2,ixOmin1,&
         ixOmin2,ixOmax1,ixOmax2,pth)
      Te(ixOmin1:ixOmax1,ixOmin2:ixOmax2)=pth(ixOmin1:ixOmax1,&
         ixOmin2:ixOmax2)/w(ixOmin1:ixOmax1,ixOmin2:ixOmax2,&
         iw_rho)*unit_temperature
      Ne(ixOmin1:ixOmax1,ixOmin2:ixOmax2)=w(ixOmin1:ixOmax1,ixOmin2:ixOmax2,&
         iw_rho)*unit_numberdensity
      flux(ixOmin1:ixOmax1,ixOmin2:ixOmax2)=Ne(ixOmin1:ixOmax1,&
         ixOmin2:ixOmax2)**2

      do ix2=ixOmin2,ixOmax2
      do ix1=ixOmin1,ixOmax1
        logT=log10(Te(ix1,ix2))
        if (logT>=t_table(1) .and. logT<=t_table(n_table)) then
          do iTt=1,n_table-1
            if (logT>=t_table(iTt) .and. logT<t_table(iTt+1)) then
              logGT=log10(f_table(iTt))*(logT-t_table(iTt+&
                 1))/(t_table(iTt)-t_table(iTt+1))+log10(f_table(iTt+&
                 1))*(logT-t_table(iTt))/(t_table(iTt+1)-t_table(iTt))
            endif
          enddo
          flux(ix1,ix2)=flux(ix1,ix2)*(10**(logGT))
        else
          flux(ix1,ix2)=0.0d0
        endif
      enddo
      enddo

      deallocate(t_table,f_table)

    end subroutine get_EUV

    subroutine get_SXR(ixImin1,ixImin2,ixImax1,ixImax2,ixOmin1,ixOmin2,ixOmax1,&
       ixOmax2,w,x,flux,El,Eu)
      !synthesize thermal SXR from El keV to Eu keV
      use mod_global_parameters

      integer, intent(in)          :: ixImin1,ixImin2,ixImax1,ixImax2,ixOmin1,&
         ixOmin2,ixOmax1,ixOmax2
      double precision, intent(in) :: x(ixImin1:ixImax1,ixImin2:ixImax2,&
         1:ndim)
      double precision             :: w(ixImin1:ixImax1,ixImin2:ixImax2,nw)
      double precision             :: flux(ixImin1:ixImax1,ixImin2:ixImax2)
      integer                      :: El,Eu

      integer :: ix1,ix2,ixO1,ixO2

      integer :: iE,numE
      double precision :: I0,kb,keV,dE,Ei
      double precision :: pth(ixImin1:ixImax1,ixImin2:ixImax2),&
         Te(ixImin1:ixImax1,ixImin2:ixImax2),kbT(ixImin1:ixImax1,&
         ixImin2:ixImax2)
      double precision :: Ne(ixImin1:ixImax1,ixImin2:ixImax2),&
         gff(ixImin1:ixImax1,ixImin2:ixImax2),fi(ixImin1:ixImax1,&
         ixImin2:ixImax2)
      double precision :: EM(ixImin1:ixImax1,ixImin2:ixImax2)

      !I0=1.07d-42    
      I0=3.01d-15
      kb=1.38d-23
      keV=1.602d-16

      dE=0.1
      numE=floor((Eu-El)/dE)

      call phys_get_pthermal(w,x,ixImin1,ixImin2,ixImax1,ixImax2,ixOmin1,&
         ixOmin2,ixOmax1,ixOmax2,pth)
      Te(ixOmin1:ixOmax1,ixOmin2:ixOmax2)=pth(ixOmin1:ixOmax1,&
         ixOmin2:ixOmax2)/w(ixOmin1:ixOmax1,ixOmin2:ixOmax2,&
         iw_rho)*unit_temperature
      Ne(ixOmin1:ixOmax1,ixOmin2:ixOmax2)=w(ixOmin1:ixOmax1,ixOmin2:ixOmax2,&
         iw_rho)*unit_numberdensity
      kbT(ixOmin1:ixOmax1,ixOmin2:ixOmax2)=kb*Te(ixOmin1:ixOmax1,&
         ixOmin2:ixOmax2)/keV
      flux(ixOmin1:ixOmax1,ixOmin2:ixOmax2)=0.0d0
      EM(ixOmin1:ixOmax1,ixOmin2:ixOmax2)=(I0*(Ne(ixOmin1:ixOmax1,&
         ixOmin2:ixOmax2))**2)

      do iE=0,numE-1
        Ei=dE*iE+El
        gff(ixOmin1:ixOmax1,ixOmin2:ixOmax2)=1
        do ix2=ixOmin2,ixOmax2
        do ix1=ixOmin1,ixOmax1
          if (kbT(ix1,ix2)<Ei) then
            gff(ix1,ix2)=(kbT(ix1,ix2)/Ei)**0.4
          endif
        enddo
        enddo
        fi(ixOmin1:ixOmax1,ixOmin2:ixOmax2)=(EM(ixOmin1:ixOmax1,&
           ixOmin2:ixOmax2)*gff(ixOmin1:ixOmax1,&
           ixOmin2:ixOmax2))* exp(-Ei/(kbT(ixOmin1:ixOmax1,&
           ixOmin2:ixOmax2)))/(Ei*sqrt(kbT(ixOmin1:ixOmax1,ixOmin2:ixOmax2)))
        flux(ixOmin1:ixOmax1,ixOmin2:ixOmax2)=flux(ixOmin1:ixOmax1,&
           ixOmin2:ixOmax2)+fi(ixOmin1:ixOmax1,ixOmin2:ixOmax2)*dE
      enddo

    end subroutine get_SXR


  

